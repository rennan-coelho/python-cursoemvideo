dig = input('Digite algo: ')
print('O tipo primitivo desse valor é ', type(dig))
print('Só tem espacos? ', dig.isspace())
print('É um número? ', dig.isnumeric())
print('É alfabetico? ', dig.isalpha())
print('É alfanumerico? ', dig.isalnum())
print('Esta em letras maiusculas? ', dig.isupper())
print('Esta em letras minusculas? ', dig.islower())
print('Esta capitalizada? ', dig.istitle())



